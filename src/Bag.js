"use strict";

class Bag {

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    contains([m, n]) {
        return this[m] && this[m][n];
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    put([m, n]) {
        if (!this[m]) {
            this[m] = {};
        }
        this[m][n] = true;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    iterate(fn) {
        for (let m in this) {
            for (let n in this[m]) {
                fn([m, n]);
            }
        }
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    getLength() {
        let l = 0;
        for (let m in this) {
            for (let n in this[m]) {
                l++;
            }
        }
        return l;
    }

}

module.exports = Bag;